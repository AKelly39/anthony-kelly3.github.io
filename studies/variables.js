/**
 * VARIABLES:
 * 0. Variables are named identifiers that can point to values of a particular type.
 * 1. To create a variable we use the keyword, var, followed by an alias.
 * 2. There are 2 phases of using variables: declaration and initialization (or assignment).
 */

// 1. Declaration //
// At the declaration phase the variable is undefined because we have not initialized it to anything
var fruit;
console.log(fruit); // prints undefined to the console

// 2. Initialization or Assignment //
// When you assign a value to an undefined variable
var myName = "Anthony";
console.log(myName); // prints "Anthony" to the console

// 3. Reassignment
// Changing or updating the value of a previously assigned variable
myName = "Kelly";
console.log(myName); // prints "Kelly" to the console


