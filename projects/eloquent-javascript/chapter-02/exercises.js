// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (var n = 1; n <= 15; n++) {
    if (n % 15 === 0){
      console.log("fizzbuzz")
    } else if (n % 5 === 0){
      console.log("buzz")
    } else if (n % 3 === 0){
      console.log("fizz")
    } else {
      console.log(n);
    }
  } 
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  var board = "";

  for (var y = 0; y < size; y++) {
    for (var x = 0; x < size; x++) {
      if ((x + y) % 2 == 0){
        board += " ";
      } else {
        board += "#";
      }
    }
  board += "\n";
  }
  console.log(board);

}

